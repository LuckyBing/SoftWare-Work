package com.example.order.Common;

public class Table {

    private int id;
    private int num;
    private int state;



    public int getId() {
        return id;
    }

    public int getNum() {
        return num;
    }

    public int getState() {
        return state;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public void setState(int state) {
        this.state = state;
    }
}